public class MultiplicationTable {
    public static void main(String[] args) {
        int number = 5;
      System.out.println("Multiplication Table of " + number + " from 1 to 100:");
      for (int i = 1; i <= 100; i++) {
        System.out.println(number + " x " + i + " = " + (number * i));
        }
      }
  }
